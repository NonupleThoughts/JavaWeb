CREATE DATABASE IF NOT EXISTS JWC_Information;

USE JWC_Information;

CREATE TABLE IF NOT EXISTS ACCOUNT(
	USERID VARCHAR(8) NOT NULL,
    PASSWORD VARCHAR(20) NOT NULL,
    NAME VARCHAR(80),
    GENDER VARCHAR(80),
    ADDR VARCHAR(80),
    BIRTHDAY DATE,
    PHONE VARCHAR(80),
    DEPARTMENT VARCHAR(80),
PRIMARY KEY(USERID));

CREATE TABLE IF NOT EXISTS ALLCURRICULUM(
	TEACHER VARCHAR(80) NOT NULL,
    CURRICULUM VARCHAR(80) NOT NULL,
    CLASSROOM VARCHAR(80),
    START TINYINT UNSIGNED,
    END TINYINT UNSIGNED,
    DAY SET('1','2','3','4','5','6','7'),
PRIMARY KEY(TEACHER, CURRICULUM));

CREATE TABLE IF NOT EXISTS STUDENTCUR(
	USERID VARCHAR(8) NOT NULL,
    CURRICULUM VARCHAR(80) NOT NULL,
    TEACHER VARCHAR(80) NOT NULL,
    GRADE DECIMAL(5,2),
PRIMARY KEY(USERID, CURRICULUM, TEACHER));

CREATE TABLE IF NOT EXISTS FILES(
	FILEID VARCHAR(80) NOT NULL,
    TYPE SMALLINT UNSIGNED NOT NULL,
    PATH VARCHAR(80) NOT NULL,
    FILENAME VARCHAR(80) NOT NULL,
PRIMARY KEY(FILEID));